#!/usr/bin/env python3
"""
Airflow DAG import script for trading_agent

This file imports all DAGs from the installed trading_agent package.
The actual DAG definitions are in: /workspace/TradingPythonAgent/src/_airflow_dags_

Generated by create-dag-imports.sh
"""

import sys
import os

# Load .env file before importing anything that might use config.py
# The .env file should be at workspace root (copied during wheel installation)
try:
    from dotenv import load_dotenv
    workspace_root = "/opt/airflow/workspace/trading_agent-workspace"
    airflow_env = os.getenv('AIRFLOW_ENV', 'dev')
    env_file_name = airflow_env
    env_file = os.path.join(workspace_root, f'.env.{env_file_name}')
    if os.path.exists(env_file):
        load_dotenv(env_file, override=True)
    else:
        # Fallback: try mounted location
        mounted_env_file = f"/workspace/infra-data-pipelines/.env.{env_file_name}"
        if os.path.exists(mounted_env_file):
            load_dotenv(mounted_env_file, override=True)
except ImportError:
    # python-dotenv not available, continue without loading .env
    pass
except Exception as e:
    # If loading fails, continue anyway
    import logging
    logging.getLogger(__name__).warning(f"Failed to load .env file: {e}")

# Add workspace root to Python path to enable imports from installed package
# Dev: package at workspace/idp-workspace/idp; test/prod: package_root/idp
workspace_root = "/opt/airflow/workspace/trading_agent-workspace"
if workspace_root and workspace_root not in sys.path:
    sys.path.insert(0, workspace_root)

# Add /opt/airflow to Python path so 'operators' package can be imported
# (operators is at /opt/airflow/operators, so we need the parent directory)
airflow_root = "/opt/airflow"
if airflow_root and airflow_root not in sys.path:
    sys.path.insert(0, airflow_root)

# Set up storage path for DAG file writes (TA)
# Prefer TRADING_AGENT_STORAGE from .env; fallback to ta/{env}/ for compatibility
import os
airflow_env = os.getenv('AIRFLOW_ENV', 'dev')
storage_env = airflow_env
default_storage_root = f"/workspace/storage-other-data/ta/{storage_env}"
if not os.getenv('TRADING_AGENT_STORAGE'):
    os.environ['TRADING_AGENT_STORAGE'] = default_storage_root

# Import all DAG objects from the installed package
# DAGs are in {package_name}/_airflow_dags_/, which Python imports as {package_name}._airflow_dags_

# trading_agent source mount: setup.py package_dir maps trading_agent -> src/
import types
for _candidate in (
    "/workspace/TradingPythonAgent/src",
    os.path.abspath(os.path.join(os.path.dirname(__file__), "../../../../TradingPythonAgent/src")),
):
    if os.path.isdir(_candidate):
        _pkg = sys.modules.get("trading_agent") or types.ModuleType("trading_agent")
        _pkg.__path__ = [_candidate]
        sys.modules["trading_agent"] = _pkg
        break


# Import DAG from dbt_feast_features_dag.py
try:
    import trading_agent._airflow_dags_.dbt_feast_features_dag as module
    globals()[module.dag.dag_id] = module.dag
except Exception as e:
    import logging
    logger = logging.getLogger(__name__)
    logger.warning(f"Failed to import/register DAG from trading_agent._airflow_dags_.dbt_feast_features_dag: {e}")

